package com.kitcenter.app.homework.lesson4.utils.convertors;

public class PrimitiveConvertor {


    public void floatToChar(float char1) {
        float floatConv2Char = (char) char1;
        System.out.println("Input float value is: " + floatConv2Char + " Output float value is: " + char1);
    }

    public void intToChar(int char1) {
        int intConv2Char = (char) char1;
        System.out.println("Input int value is: " + intConv2Char + " Output int value is: " + char1);
    }

    public void charToInt(char int1) {
        char charConv2int = int1;
        System.out.println("Input char value is: " + charConv2int + " Output char value is: " + int1);
    }
}
