package com.kitcenter.app.homework.lesson4.utils.convertors;

public class PrimitiveConvertor {


    public void floatToChar(float f1) {
        float f1 = (char) char1;
        System.out.println("Input float value is: " + f1 + " Output float value is: " + f1);
    }

    public void intToChar(int in1) {
        int int1 = (char) char1;
        System.out.println("Input int value is: " + int1 + " Output int value is: " + int1);
    }

    public void charToInt(char char1) {
        char char1 = (int) int1;
        System.out.println("Input char value is: " + char1 + " Output char value is: " + char1);
    }
}
